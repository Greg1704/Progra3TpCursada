package atributosFormularios;

public class Edad {
	private int edad;
	private int peso;
	
	public Edad(int edad,int peso) {
		this.edad = edad;
		this.peso = peso;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	

}

